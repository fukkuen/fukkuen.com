<p class="my-4">《聽到聽不到》是一個以聲音創作連繫四位不同能力的藝術家的合作項目，由聲音圖書館及香港展能藝術會策辦。四位藝術家包括來自聲音圖書館的我和張才生，以及來自香港展能藝術會的王志威和羅志琪。合作計劃環跨5個月，包括由最初我和張才生為二人設計的聲音工作坊，到後來4人共同創作傳波仔，到創作交流結小有成果舉行展覽，還有因而得到的聲音友誼。</p>

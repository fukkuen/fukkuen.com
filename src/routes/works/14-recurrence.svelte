<p class="my-4">電光火石之間，物件緩緩地上升、急速的下墜、回彈的漣漪以及他者的介入，三人玩聲組合「波寺」利用這些元素來回地製造有偏差的期望。一起搬弄著裡面的空氣和聲音，使14層中的一切都循環著，就像蠕動著的一條垂直的隧道，再而從橫向旋轉與垂直升降之間找到連結。三人於刻意安排與即興之間遊走，以人與物件之間的互動產生不同程度的迴響，這迴響看似推動著某種純粹的狀態。</p>

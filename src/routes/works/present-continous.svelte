<p>我經常問：在還是不在？在可能是不在，不在也不一定是不在。</p>
<p>捕捉這一種在的狀態像是一種使命。</p>
<p>透過裝置、即興及演出，我描述了一些微型狀態，請打開感觀，把經驗放大成為你腦海中的短暫新世界。</p>
<h2>臨界領域 | Into (裝置 | Installation)</h2>
<img src="/present-continusous-6.jpg" alt="">
<img src="present-continuous-01.jpg" alt="">
<p>面對不可抗拒的引力及撲面的阻力，僵持在臨界的領域，靜止中潛伏著撕裂的張力。</p>
